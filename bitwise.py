n1=int(input("Enter First Number :"))
n2=int(input("Enter Second Number :"))

print("{} & {} = {}".format(n1,n2,n1&n2))
print("{} | {} = {}".format(n1,n2,n1|n2))
print("{} ^ {} = {}".format(n1,n2,n1^n2))
print("~ {} = {}".format(n1,~n1))
print(" {} << 2 = {}".format(n1,n1<<2))
print(" {} >> 2 = {}".format(n1,n1>>2))
